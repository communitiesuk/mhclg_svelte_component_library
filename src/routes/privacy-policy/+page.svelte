      <h1 class="govuk-heading-l">Privacy Notice</h1>

      <p class="govuk-body">
        The following is to explain your rights and give you the information you
        are entitled to under UK data protection legislation.
      </p>

      <h2 class="govuk-heading-m">
        The identity and contact details of the Ministry of Housing, Communities
        and Local Government (MHCLG) and our Data Protection Officer
      </h2>

      <p class="govuk-body">
        MHCLG is the data controller. The Data Protection Officer can be
        contacted at
        <a href="mailto:dataprotection@communities.gov.uk" class="govuk-link"
          >dataprotection@communities.gov.uk</a
        >
      </p>

      <p class="govuk-body">
        If you follow a link to a service provided by another government
        department, agency or local authority, that organisation will:
      </p>

      <ul class="govuk-list govuk-list--bullet">
        <li>be the data controller</li>
        <li>be responsible for processing any data you share with them</li>
        <li>
          publish and manage their own privacy notice with details of how to
          contact them
        </li>
      </ul>

      <p class="govuk-body">
        A data controller determines how and why personal data is processed. For
        more information, read the MHCLG's (formerly DLUHC) entry in the
        <a
          href="https://ico.org.uk/ESDWebPages/Entry/Z7123035"
          rel="external"
          target="_blank"
          class="govuk-link">Data Protection Public Register</a
        >.
      </p>

      <h2 class="govuk-heading-m">What data we collect</h2>

      <p class="govuk-body">
        We collect information about how you use our website to help us improve
        the site and ensure it meets the needs of its users.
      </p>

      <h3 class="govuk-heading-s">Google Analytics 4</h3>

      <p class="govuk-body">
        We use cookies and page tagging techniques to enable Google Analytics 4
        to collect information such as:
      </p>

      <ul class="govuk-list govuk-list--bullet">
        <li>The pages you visit</li>
        <li>How long you engage with the pages</li>
        <li>How you arrived at the site</li>
        <li>How you interact with the content</li>
        <li>The files you download</li>
        <li>What you search for on the site</li>
      </ul>

      <p class="govuk-body">
        We do not store your personal information through Google Analytics 4,
        for example, your name, address or internet protocol (IP) address. We do
        not allow Google Analytics 4 to use or share data about how you use the
        site. We will not identify you through analytics information, and we
        will not combine analytics information with other datasets in a way that
        would identify who you are.
      </p>

      <h3 class="govuk-heading-s">On-site feedback forms</h3>

      <p class="govuk-body">
        We use feedback forms on the site to allow you to send direct feedback
        or queries to our communication team, for example, via the feedback link
        in the website banner. As part of this service, we collect information
        such as:
      </p>

      <ul class="govuk-list govuk-list--bullet">
        <li>questions, queries or feedback you leave.</li>
        <li>your name and email address if you choose to provide it.</li>
      </ul>

      <p class="govuk-body">
        We only use the personal data you provide to respond to your query.
      </p>

      <h2 class="govuk-heading-m">Why we need your data</h2>

      <p class="govuk-body">
        We collect information through these tools to help:
      </p>

      <ul class="govuk-list govuk-list--bullet">
        <li>Make sure the site is meeting the needs of its users</li>
        <li>Make improvements to the website</li>
        <li>Gather feedback to improve our services</li>
        <li>Respond to any feedback you send us, if you have asked us to</li>
        <li>Monitor use of the site to identify security threats</li>
        <li>Detect, diagnose, and fix issues with the website.</li>
        <li>
          Monitor the website's performance to ensure a smooth user experience.
        </li>
      </ul>

      <h2 class="govuk-heading-m">Our legal basis for processing your data</h2>

      <p class="govuk-body">
        The legal basis for processing personal data in relation to site
        security, error-handling and performance are our legitimate interests,
        and the legitimate interests of our users, in ensuring the security and
        integrity of our site
      </p>

      <p class="govuk-body">
        The legal basis for processing data collected with Google Analytics and
        on-site feedback forms is your consent.
      </p>

      <p class="govuk-body">
        The legal basis for processing all other personal data is that it's
        necessary:
      </p>

      <ul class="govuk-list govuk-list--bullet">
        <li>to perform a task in the public interest</li>
        <li>in the exercise of our functions as a government department</li>
      </ul>

      <h2 class="govuk-heading-m">What we do with your data</h2>

      <p class="govuk-body">
        The data we collect may be shared with other government departments,
        agencies and public bodies. It may also be shared with our technology
        suppliers, for example our hosting provider.
      </p>

      <p class="govuk-body">
        We will share your data if we are required to do so by law - for
        example, by court order, or to prevent fraud or other crime.
      </p>

      <p class="govuk-body">
        The data we collect with Google Analytics cookies is transferred and
        stored with Google where we analyse it with Google Analytics software
        (Google Analytics 4). We do not allow Google to use or share this data
        for their own purposes.
      </p>

      <p class="govuk-body">We will not:</p>

      <ul class="govuk-list govuk-list--bullet">
        <li>sell or rent your data to third parties</li>
        <li>share your data with third parties for marketing purposes</li>
      </ul>

      <h2 class="govuk-heading-m">How long we keep your data</h2>

      <h3 class="govuk-heading-s">Google Analytics 4</h3>

      <p class="govuk-body">
        Data collected by Google Analytics 4 are retained by Google for 14
        months. After this point, the data are deleted from the Google Analytics
        4 server.
      </p>

      <p class="govuk-body">
        Data are extracted from Google Analytics 4 and stored in secure MHCLG
        databases where the retention period is for as long as necessary to
        fulfill the purposes outlined above.
      </p>

      <h2 class="govuk-heading-m">Your rights</h2>

      <p class="govuk-body">
        The data we are collecting is your personal data, and you have rights
        that affect what happens to it. You have the right to:
      </p>

      <ul class="govuk-list govuk-list--bullet">
        <li>know that we are using your personal data</li>
        <li>see what data we have about you</li>
        <li>
          ask to have your data corrected, and to ask how we check the
          information we hold is accurate
        </li>
        <li>complain to the ICO (see below)</li>
      </ul>

      <p class="govuk-body">
        In some circumstances you may also have the right to withdraw your
        consent to us having or using your data, to have all data about you
        deleted, or to object to particularly types of use of your data. We will
        tell you when these rights apply.
      </p>

      <p class="govuk-body">
        If you have any of these requests, contact
        <a
          href="mailto:dataexplorerfeedback@communities.gov.uk"
          class="govuk-link">dataexplorerfeedback@communities.gov.uk</a
        >.
      </p>

      <h2 class="govuk-heading-m">Automated decision making</h2>

      <p class="govuk-body">
        We will not use your data for any automated decision making.
      </p>

      <h2 class="govuk-heading-m">Where your data are processed and stored</h2>

      <p class="govuk-body">
        We design, build and run our systems to make sure that your data are as
        safe as possible at all stages, both while they are processed and when
        they are stored.
      </p>

      <p class="govuk-body">
        All personal data are stored in the
        <a
          href="https://www.gov.uk/eu-eea"
          rel="external"
          target="_blank"
          class="govuk-link">European Economic Area (EEA)</a
        >. Data collected by Google Analytics 4 may be transferred outside the
        UK and EEA for processing.
      </p>

      <p class="govuk-body">
        Where data processing happens in the United States (US), suppliers need
        to meet the certification standards set out in the UK-US data bridge
        agreement. This facilitates flows of personal data to certified US
        companies that have opted in to the
        <a
          href="https://www.gov.uk/government/publications/uk-us-data-bridge-supporting-documents/uk-us-data-bridge-explainer"
          rel="external"
          target="_blank"
          class="govuk-link">EU-US Data Privacy Framework</a
        >.
      </p>

      <h2 class="govuk-heading-m">
        How we protect your data and keep it secure
      </h2>

      <p class="govuk-body">
        We are committed to doing all that we can to keep your data secure. We
        have set up systems and processes to prevent unauthorised access or
        disclosure of your data.
      </p>

      <p class="govuk-body">
        We also make sure that any third parties that we deal with keep all
        personal data they process on our behalf secure.
      </p>

      <h2 class="govuk-heading-m">Complaints and more information</h2>

      <p class="govuk-body">
        When we ask you for information, we will keep to the law, including the
        Data Protection Act 2018 and UK General Data Protection Regulation.
      </p>

      <p class="govuk-body">
        If you are unhappy with the way the Department has acted, you can
        <a
          href="https://www.gov.uk/government/organisations/ministry-of-housing-communities-local-government/about/complaints-procedure"
          rel="external"
          target="_blank"
          class="govuk-link">make a complaint.</a
        >
      </p>

      <p class="govuk-body">
        If you are not happy with how we are using your personal data, you
